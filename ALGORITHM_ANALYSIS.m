% 严格的数学分析：低频Z和S矩阵外推算法推导
% ========================================================

% 【物理基础】
% 在低频下（f→0），网络参数的物理特性：
%
% 1. Z矩阵在低频的行为：
%    - 纯电阻元素：Z_ii(f→0) → R_dc（直流电阻，常数）
%    - 纯电感元素：Z_ij(f→0) → 0（高频时主导）
%    - 纯容性元素：Z_ij(f→0) → ∞（低频时阻抗无穷大）
%    - 一般情况：Z = R + jωL - j/(ωC)
%
% 2. S矩阵在低频的行为：
%    - S与Z的关系：S = (Z/Z0 - I)/(Z/Z0 + I)
%    - 当Z的某些分量趋于∞时，S→1（开路）
%    - 当Z的某些分量趋于0时，S→-1（短路）
%    - 当Z = Z0时，S = 0（匹配）

% 【关键问题分析】
% 原代码问题：
% 1. 直接外推Z后转换为S，导致Z矩阵变得非常大（高阻抗）
% 2. Z转S时矩阵接近奇异（Z/Z0 + I的行列式接近0）
% 3. 输出全NaN
%
% 原因：不应该对整个Z矩阵进行矩阵求逆，而应该：
% - 对每个元素单独处理
% - 利用原始S中已有的低频信息
% - 只对那些有明显跳变的元素进行约束外推

% 【严格的数学推导】
% 
% 第一步：分析Z到S的元素级转换
% ================================
% 对于2端口网络：
%   S11 = (Z11 - Z0)(Z22 - Z0) - Z12*Z21  /  D
%   S12 = 2*Z12*Z0  /  D
%   其中 D = (Z11 + Z0)(Z22 + Z0) - Z12*Z21
%
% 对于N端口网络的一般情况，使用全矩阵形式：
%   S = (Z*Z0^{-1} - I)(Z*Z0^{-1} + I)^{-1}
%
% 问题：当Z矩阵某些元素变得非常大时，Z*Z0^{-1}变成大矩阵
%       (Z*Z0^{-1} + I)的行列式变得很小甚至接近0
%
% 第二步：在低频下保持S矩阵连续性的约束
% ==========================================
% 原始S在低频已包含了完整信息，我们的目标是：
% 1. 修改Z矩阵使其曲线更平滑
% 2. 同时保证转换回S后基本不变
%
% 关键洞察：
% - 如果S(f_low)已经给定，我们应该先在S域做外推
% - 然后才转回Z域
% - 而不是先在Z域外推再转S
%
% 第三步：在S域进行物理约束外推
% ================================
% 低频时S的特性：
% - 对于开路（Z→∞）：S→1
% - 对于短路（Z→0）：S→-1  
% - 对于纯电阻（Z=R）：S = (R/Z0-1)/(R/Z0+1)
%
% 在原始S中检测跳变类型：
%   - 如果|S|接近1且频率下降时|S|持续增大 → 开路/容性
%   - 如果|S|从小变大 → 可能是测量噪声
%   - 如果相位持续变化 → 混合特性
%
% 第四步：平滑约束的正确形式
% =============================
% 对于有跳变的频点k_low < M_opt：
%
% A. 获取参考点数据（平滑频段起始点M_opt）
%    S_ref(i,j) - 从原始数据
%    Z_ref(i,j) = Z0*(I + S_ref)/(I - S_ref)
%
% B. 对S进行Kramers-Kronig兼容的平滑外推
%    在低频域使用多项式或样条拟合
%    确保：
%    - arg(S)连续变化（不跳跃）
%    - |S|单调或平缓变化
%    - |S| ≤ 1（无源性）
%
% C. 最后从平滑的S转换为Z
%    Z_smooth(i,j) = Z0*(I + S_smooth)/(I - S_smooth)

% ========================================================
% 改进的外推算法
% ========================================================

